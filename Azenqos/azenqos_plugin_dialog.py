    def initGraduatedPattern(self, layer, graduatedPattern):
        ranges = []

        if len(graduatedPattern["range"]) == 0:
            gc.azenqosDatabase.open()
            result = []
            queryString = "SELECT %s FROM '%s' WHERE %s IS NOT NULL" % (
                graduatedPattern["expression"],
                graduatedPattern["table"],
                graduatedPattern["expression"],
            )
            query = QSqlQuery()
            query.exec_(queryString)
            while query.next():
                result.append(query.value(0))

            # distinct result
            result = list(set(result))
            result.sort(reverse=True)
            for itemRange in result:
                itemRange = int(itemRange)
                symbol = QgsSymbol.defaultSymbol(layer.geometryType())
                symbol.setColor(QColor(get_default_color_for_index(itemRange)))
                rng = QgsRendererRange(
                    itemRange, itemRange, symbol, "%d" % (itemRange),
                )
                ranges.append(rng)

            gc.azenqosDatabase.close()
        else:
            for itemRange in graduatedPattern["range"]:
                symbol = QgsSymbol.defaultSymbol(layer.geometryType())
                symbol.setColor(QColor(itemRange["color"]))
                rng = QgsRendererRange(
                    itemRange["from"],
                    itemRange["to"],
                    symbol,
                    "%d - %d" % (itemRange["from"], itemRange["to"]),
                )
                ranges.append(rng)

        # create the renderer and assign it to a layer
        expression = graduatedPattern["expression"]  # field name
        renderer = QgsGraduatedSymbolRenderer(expression, ranges)
        layer.setRenderer(renderer)
        iface.mapCanvas().refresh()

