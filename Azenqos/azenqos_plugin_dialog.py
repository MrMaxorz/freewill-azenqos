def datetimeStringtoTimestamp(datetimeString: str):
    try:
        element = datetime.datetime.strptime(datetimeString, "%Y-%m-%d %H:%M:%S.%f")
        timestamp = datetime.datetime.timestamp(element)
        return timestamp
    except Exception as ex:
        print(ex)
        return False


