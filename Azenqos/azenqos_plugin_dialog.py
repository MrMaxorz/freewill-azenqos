class TableModel(QAbstractTableModel):
    def __init__(self, inputData, header, parent=None, *args):
        QAbstractTableModel.__init__(self, parent, *args)
        self.headerLabels = header
        self.dataSource = inputData
        # self.testColumnValue()

    def rowCount(self, parent):
        rows = 0
        if self.dataSource:
            rows = len(self.dataSource)
        return rows

    def columnCount(self, parent):
        columns = 0
        if self.headerLabels:
            columns = len(self.headerLabels)
        return columns

    def data(self, index, role):
        if not index.isValid():
            return QVariant()
        elif role != Qt.DisplayRole:
            return QVariant()
        if index:
            row = index.row()
            column = index.column()
            return QVariant(self.dataSource[row][column])

    def dataString(self, index):
        return self.dataSource[index.row()][index.column()]

    def headerData(self, section, orientation, role=Qt.DisplayRole):
        if role == Qt.DisplayRole and orientation == Qt.Horizontal:
            return self.headerLabels[section]
        return QAbstractTableModel.headerData(self, section, orientation, role)


class FilterHeader(QtGui.QHeaderView):
    filterActivated = QtCore.pyqtSignal()

    def __init__(self, parent):
        super().__init__(QtCore.Qt.Horizontal, parent)
        self._editors = []
        self._padding = 4
        self.setStretchLastSection(True)
        self.setSectionsClickable(True)
        self.setHighlightSections(True)
        self.setResizeMode(QHeaderView.Interactive)
        # self.setResizeMode(QtGui.QHeaderView.Stretch)
        # self.setDefaultAlignment(
        #     QtCore.Qt.AlignLeft | QtCore.Qt.AlignVCenter)
        self.setSortIndicatorShown(True)
        self.sectionResized.connect(self.adjustPositions)
        parent.horizontalScrollBar().valueChanged.connect(self.adjustPositions)

    def setFilterBoxes(self, count, parent):
        while self._editors:
            editor = self._editors.pop()
            editor.deleteLater()
        for index in range(count):
            editor = QtGui.QLineEdit(self.parent())
            editor.setPlaceholderText("Filter")
            editor.textChanged.connect(
                lambda text, col=index: parent.proxyModel.setFilterByColumn(
                    QRegExp(text, Qt.CaseInsensitive, QRegExp.FixedString), col
                )
            )
            editor.textChanged.connect(self.adjustPositions)
            self._editors.append(editor)
        self._verticalWidth = parent.tableView.verticalHeader().sizeHint().width()
        self.adjustPositions()

    def sizeHint(self):
        size = super().sizeHint()
        if self._editors:
            height = self._editors[0].sizeHint().height()
            size.setHeight(size.height() + height + self._padding)
        return size

    def updateGeometries(self):
        if self._editors:
            height = self._editors[0].sizeHint().height()
            self.setViewportMargins(0, 0, 0, height + self._padding)
        else:
            self.setViewportMargins(0, 0, 0, 0)
        super().updateGeometries()
        self.adjustPositions()

    def adjustPositions(self, dummy=None):
        for index, editor in enumerate(self._editors):
            height = editor.sizeHint().height()
            editor.move(
                self.sectionPosition(index) - self.offset() + self._verticalWidth,
                height + (self._padding // 2),
            )
            editor.resize(self.sectionSize(index), height)

    def filterText(self, index):
        if 0 <= index < len(self._editors):
            return self._editors[index].text()
        return ""

    def setFilterText(self, index, text):
        if 0 <= index < len(self._editors):
            self._editors[index].setText(text)

    def clearFilters(self):
        for editor in self._editors:
            editor.clear()


class TimeSliderThread(QThread):
    changeValue = pyqtSignal(float)

    def __init__(self):
        QThread.__init__(self)
        self.currentSliderValue = None

    def __del__(self):
        self.wait()

    def run(self):
        self.playTime()

    def playTime(self):
        timeSlider.setDisabled(True)
        global isSliderPlay, fastForwardValue, slowDownValue
        sleeptime = 1 / fastForwardValue
        # isSliderPlay = True
        if isSliderPlay:
            if self.currentSliderValue:
                for x in np.arange(
                    self.currentSliderValue, sliderLength, (1 * slowDownValue)
                ):
                    if not isSliderPlay:
                        break
                    else:
                        time.sleep(sleeptime)
                        value = timeSlider.value() + (1 * slowDownValue)
                        self.changeValue.emit(value)

                    if x >= sliderLength:
                        isSliderPlay = False
                        break
            else:
                for x in np.arange(0, sliderLength, (1 * slowDownValue)):
                    if not isSliderPlay:
                        break
                    else:
                        time.sleep(sleeptime)
                        value = timeSlider.value() + (1 * slowDownValue)
                        self.changeValue.emit(value)

                    if x >= sliderLength:
                        isSliderPlay = False
                        break
        else:
            self.quit()

    def set(self, value):
        self.currentSliderValue = value


class LayerTask(QgsTask):
    def __init__(self, desc, databasePath):
        QgsTask.__init__(self, desc)
        self.dbPath = databasePath
        self.start_time = None
        self.desc = desc
        self.exception = None
        self.vLayers = []

    def addMapToQgis(self):
        # urlWithParams = 'type=xyz&url=http://a.tile.openstreetmap.org/%7Bz%7D/%7Bx%7D/%7By%7D.png&zmax=19&zmin=0&crs=EPSG3857'
        urlWithParams = "contextualWMSLegend=0&crs=EPSG:4326&dpiMode=7&featureCount=10&format=image/tiff&layers=longdo_icons&styles&url=http://ms.longdo.com/mapproxy/service"
        rlayer = QgsRasterLayer(urlWithParams, "Street map", "wms")
        if rlayer.isValid():
            QgsProject.instance().addMapLayer(rlayer)
            # self.azqGroup.addLayer(rlayer)
        else:
            QgsMessageLog.logMessage("Invalid layer")

    def run(self):
        QgsMessageLog.logMessage("[-- Start add layers --]", tag="Processing")
        self.start_time = time.time()
        global allLayers
        global vLayers

        return True

    def finished(self, result):
        global tableList, mostFeaturesLayer
        if result:
            mostFeaturesLayer = None
            self.addMapToQgis()
            uri = QgsDataSourceUri()
            uri.setDatabase(self.dbPath)
            allLayers.sort(reverse=True)
            geom_column = "geom"
            for tableName in allLayers:
                uri.setDataSource("", tableName, geom_column)
                vlayer = iface.addVectorLayer(uri.uri(), tableName, "spatialite")
                features = vlayer.featureCount()
                if mostFeaturesLayer is None:
                    mostFeaturesLayer = (tableName, features)
                elif features > mostFeaturesLayer[1]:
                    mostFeaturesLayer = (tableName, features)

                if vlayer:
                    symbol_renderer = vlayer.renderer()
                    if symbol_renderer:
                        symbol = symbol_renderer.symbol()
                        symbol.setColor(QColor(125, 139, 142))
                        symbol.symbolLayer(0).setStrokeColor(QColor(0, 0, 0))
                        symbol.setSize(2.4)
                    iface.layerTreeView().refreshLayerSymbology(vlayer.id())
                    vlayer.triggerRepaint()
                    if not tableName in tableList:
                        tableList.append(tableName)

            iface.mapCanvas().setSelectionColor(QColor("yellow"))

            elapsed_time = time.time() - self.start_time
            QgsMessageLog.logMessage(
                "Elapsed time: " + str(elapsed_time) + " s.", tag="Processing"
            )
            QgsMessageLog.logMessage("[-- End add layers --]", tag="Processing")
        else:
            if self.exception is None:
                QgsMessageLog.logMessage(
                    'Task "{name}" not successful but without '
                    "exception (probably the task was manually "
                    "canceled by the user)".format(name=self.desc),
                    tag="Exception",
                )
            else:
                QgsMessageLog.logMessage(
                    'Task "{name}" Exception: {exception}'.format(name=self.desc),
                    exception=self.exception,
                    tag="Exception",
                )
                raise self.exception


